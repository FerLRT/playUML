import { DataTypes } from "sequelize";
import { sequelize } from "../config/dbConfig.js";

export const Achievement = sequelize.define("achievements", {
  id: {
    type: DataTypes.INTEGER,
    allowNull: false,
    primaryKey: true,
    autoIncrement: true,
  },
  name: {
    type: DataTypes.STRING,
    allowNull: false,
  },
  description: {
    type: DataTypes.TEXT,
    allowNull: true,
  },
});
